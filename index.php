<script>
document.addEventListener('DOMContentLoaded', function() {
    document.getElementById('formulario').addEventListener('submit', function(event) {
        event.preventDefault(); // Evita el comportamiento predeterminado de envío del formulario

        // Obtener los valores de los campos del formulario
        var formData = new FormData(this); // Obtiene los datos del formulario

        // Configura la solicitud AJAX
        var xhr = new XMLHttpRequest();
        xhr.open('POST', 'guardar_datos.php', true);

        // Define el evento de carga
        xhr.onload = function() {
            if (xhr.status >= 200 && xhr.status < 400) {
                // Muestra la alerta de éxito
                alert("Registro exitoso");
                // Limpia el formulario
                document.getElementById('formulario').reset();
            }
        };

        // Envía la solicitud AJAX con los datos del formulario
        xhr.send(formData);
    });
});


    </script>
